class Solution {
  public:
    vector<int> Solve(int n, vector<int>&arr) {
        map<int,int>freq;
        int cnt = 0;
        vector<int>ans;
        for(auto &x:arr) freq[x]++;
        for(auto &p : freq) if(p.second > n/3) ans.push_back(p.first); 
        if(ans.size()==0) return {-1};
        return ans;
    }
};

